public class ModalClass {

	public String regionCode;
	public String weatherParam;
	public String year;
	public String keyMonth;
	public String value;

	public String getRegionCode() {
		return regionCode;
	}

	public void setRegionCode(String regionCode) {
		this.regionCode = regionCode;
	}

	public String getWeatherParam() {
		return weatherParam;
	}

	public void setWeatherParam(String weatherParam) {
		this.weatherParam = weatherParam;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getKeyMonth() {
		return keyMonth;
	}

	public void setKeyMonth(String keyMonth) {
		this.keyMonth = keyMonth;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
